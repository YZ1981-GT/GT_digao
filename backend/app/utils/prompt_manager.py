"""提示词管理工具 - 统一管理所有 AI 提示词模板"""

# 通用的目录生成 JSON 格式要求
_OUTLINE_JSON_SCHEMA = """{
    "outline": [
      {
        "id": "1",
        "title": "",
        "description": "",
        "children": [
          {
            "id": "1.1",
            "title": "",
            "description": "",
            "children":[
                {
                  "id": "1.1.1",
                  "title": "",
                  "description": ""
                }
            ]
          }
        ]
      }
    ]
  }"""

# 通用的目录生成要求
_OUTLINE_BASE_REQUIREMENTS = """要求：
  1. 目录结构要全面覆盖审计文档的所有必要章节
  2. 章节名称要专业、准确，符合审计文档规范
  3. 一级目录名称要与文档结构要求中的章节名称一致，如果文档结构要求中没有章节名称，则结合文档内容要求，生成一级目录名称
  4. 一共包括三级目录
  5. 返回标准JSON格式，包含章节编号、标题、描述和子章节
  6. 除了JSON结果外，不要输出任何其他内容"""


def _build_outline_system_prompt(extra_instruction: str = "") -> str:
    """构建目录生成的 system prompt"""
    base = f"""你是一个专业的审计文档编写专家。根据提供的项目概述和文档结构要求，生成审计文档的目录结构。
  {extra_instruction}
  {_OUTLINE_BASE_REQUIREMENTS}

  JSON格式要求：
  {_OUTLINE_JSON_SCHEMA}
  """
    return base


def read_expand_outline_prompt():
    """从简版文档方案中提取目录的提示词"""
    system_prompt = """你是一个专业的审计文档编写专家。需要从用户提交的文档方案中，提取出目录结构。

  要求：
  1. 目录结构要全面覆盖审计文档的所有必要目录，包含多级目录
  2. 如果文档方案中有章节名称，则直接使用文档方案中的章节名称
  3. 如果文档方案中没有章节名称，则结合全文，总结出章节名称
  4. 返回标准JSON格式，包含章节编号、标题、描述和子章节，注意编号要连贯
  5. 除了JSON结果外，不要输出任何其他内容

  JSON格式要求：
  """ + _OUTLINE_JSON_SCHEMA
    return system_prompt


def generate_outline_prompt(overview: str, requirements: str):
    """生成目录的提示词（无参考目录）"""
    system_prompt = _build_outline_system_prompt()

    user_prompt = f"""请基于以下项目信息生成审计文档目录结构：

  项目概述：
  {overview}

  文档结构要求：
  {requirements}

  请生成完整的审计文档目录结构，确保覆盖所有文档要求要点。"""
    return system_prompt, user_prompt


def generate_outline_with_old_prompt(overview: str, requirements: str, old_outline: str, old_document: str = ""):
    """生成目录的提示词（有参考目录）"""
    extra = "用户会提供一个自己编写的目录，你要保证目录满足文档结构要求，并充分结合用户自己编写的目录。"
    system_prompt = _build_outline_system_prompt(extra)

    # 如果有原始文档内容，截取前部分作为参考（避免 token 超限）
    doc_section = ""
    if old_document and old_document.strip():
        max_doc_len = 8000
        truncated_doc = old_document[:max_doc_len] + ("...(内容已截断)" if len(old_document) > max_doc_len else "")
        doc_section = f"""
  用户原始文档内容（供参考，帮助理解目录含义）：
  {truncated_doc}
"""

    user_prompt = f"""请基于以下项目信息生成审计文档目录结构：
  用户自己编写的目录：
  {old_outline}
{doc_section}
  项目概述：
  {overview}

  文档结构要求：
  {requirements}

  请生成完整的审计文档目录结构，确保覆盖所有文档要求要点。"""
    return system_prompt, user_prompt


