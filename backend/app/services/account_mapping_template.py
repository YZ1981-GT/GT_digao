def _normalize(name: str) -> str:
    """去除特殊前缀标记、括号说明文字和空白。

    报表科目名称常带括号说明，如 "资产处置收益(损失以"-"号填列）"，
    必须去掉才能正确匹配模板关键词。
    """
    import re
    # 去掉中英文括号及其内容
    name = re.sub(r'[（(][^）)]*[）)]', '', name)
    return name.replace('△', '').replace('▲', '').replace('*', '').replace('#', '').strip()


class AccountMappingTemplate:
    """标准科目-附注映射查询器。"""

    def __init__(self) -> None:
        # 构建 normalized_name -> keywords 的映射
        self._map: Dict[str, List[str]] = {}
        for acct, keywords in _ALL_MAPPINGS.items():
            self._map[_normalize(acct)] = keywords

    def get_keywords(self, account_name: str) -> Optional[List[str]]:
        """根据报表科目名称获取附注匹配关键词列表。"""
        norm = _normalize(account_name)
        # 精确匹配
        if norm in self._map:
            return self._map[norm]
        # 包含匹配（报表科目名可能带"其中："等前缀），优先选择最长匹配
        best_kws: Optional[List[str]] = None
        best_len = 0
        for key, kws in self._map.items():
            if key in norm or norm in key:
                if len(key) > best_len:
                    best_kws = kws
                    best_len = len(key)
        return best_kws

    def match_note(
        self,
        statement_account: str,
        note_account: str,
        note_section: str = '',
    ) -> bool:
        """判断某个附注是否匹配给定的报表科目。

        精确匹配优先：如果附注科目名称与另一个模板科目完全一致，
        则只有该科目才能匹配，避免 "营业外收入" 被 "营业收入" 的关键词误匹配。
        """
        keywords = self.get_keywords(statement_account)
        if not keywords:
            return False

        norm_note = _normalize(note_account)
        norm_stmt = _normalize(statement_account)

        # 如果附注科目名称与报表科目完全一致，直接匹配
        if norm_note == norm_stmt:
            return True

        # 如果附注科目名称恰好是模板中另一个科目的精确名称，
        # 则不允许当前科目匹配（应由那个精确科目来匹配）。
        # 例外：组合科目（如"营业收入、营业成本"）应允许其组成科目匹配。
        if norm_note in self._map and norm_note != norm_stmt:
            # 检查是否为组合科目：附注科目名包含当前报表科目名
            if norm_stmt not in norm_note:
                return False

        target = norm_note + ' ' + _normalize(note_section or '')
        for kw in keywords:
            if kw in target:
                return True
        return False

    def all_accounts(self) -> List[str]:
        """返回所有标准科目名称。"""
        return list(self._map.keys())

    def get_parent_company_accounts(self, template_type: str) -> List[Dict[str, object]]:
        """获取母公司附注预设科目清单。

        Args:
            template_type: 'soe' 或 'listed'

        Returns:
            科目列表，每项包含 name, keywords, order
        """
        return _PARENT_ACCOUNTS_MAP.get(template_type, [])

    def get_consolidated_accounts(self, template_type: str) -> List[Dict[str, object]]:
        """获取合并附注预设科目清单。

        Args:
            template_type: 'soe' 或 'listed'

        Returns:
            科目列表，每项包含 name, keywords, order
        """
        return _CONSOLIDATED_ACCOUNTS_MAP.get(template_type, [])

    def match_parent_note(
        self,
        note_title: str,
        template_type: str,
    ) -> Optional[str]:
        """判断附注标题是否匹配母公司预设科目，返回匹配的科目名。"""
        accounts = self.get_parent_company_accounts(template_type)
        norm = _normalize(note_title)
        for acct in accounts:
            for kw in acct['keywords']:
                if kw in norm:
                    return acct['name']
        return None


